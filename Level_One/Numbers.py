# Addition
print('5 + 2 = ', 5 + 2)

# Subtraction
print('5 - 2 = ', 5 - 2)

# Multiplication
print('5 * 2 = ', 5 * 2)

# Division
print('5 / 2 = ', 5 / 2)

# Exponent
print('5 ^ 2 = ', 5 ** 2)
# This is the same as 5 * 5

# Ordinary Division
print('5 / 2 = ', 5 / 2)

# Floor Division
print('5 // 2 = ', 5 // 2, ' (which will get the integer number of the division only)')

# Modulus
print('5 % 2 = ', 5 % 2)

